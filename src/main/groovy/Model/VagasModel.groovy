package Model

class VagasModel {
    String nome
    String descricao
    double salario
    String nomeEmpresa

    VagasModel(String nome, String descricao, double salario, String nomeEmpresa) {
        this.nome = nome
        this.descricao = descricao
        this.salario = salario
        this.nomeEmpresa = nomeEmpresa
    }
}